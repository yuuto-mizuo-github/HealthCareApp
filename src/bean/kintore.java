package bean;


public class kintore {
	

/** スクワット */
private String squat;
/** 腹筋 */
private String  situp;
/**腕立て伏せ**/
private String pushups;
/**ランニング**/
private String running;
/**日付**/
private String deadline;
public String getSquat() {
	return squat;
}
public void setSquat(String squat) {
	this.squat = squat;
}
public String getSitup() {
	return situp;
}
public void setSitup(String situp) {
	this.situp = situp;
}
public String getPushups() {
	return pushups;
}
public void setPushups(String pushups) {
	this.pushups = pushups;
}
public String getRunning() {
	return running;
}
public void setRunning(String running) {
	this.running = running;
}
public String getDeadline() {
	return deadline;
}
public void setDeadline(String deadline) {
	this.deadline = deadline;
}
}
